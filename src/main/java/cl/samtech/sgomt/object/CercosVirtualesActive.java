package cl.samtech.sgomt.object;

public class CercosVirtualesActive {
	
	private int idGeo;
	private String nombre;
	private String tipo;
	private String color;
	private int area;
	
	
	public int getIdGeo() {
		return idGeo;
	}
	public void setIdGeo(int idGeo) {
		this.idGeo = idGeo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getArea() {
		return area;
	}
	public void setArea(int area) {
		this.area = area;
	}
	

}
