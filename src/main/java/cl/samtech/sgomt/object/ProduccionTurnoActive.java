package cl.samtech.sgomt.object;

public class ProduccionTurnoActive {
	
	private Integer geo_ini;
	private String geo_nom_ini;
	private Integer geo_fin;
	private String geo_nom_fin;
	private String patente;
	private Long vuelta;
	private Long segundociclo;
	private double carga;
	
	
	public Integer getGeo_ini() {
		return geo_ini;
	}
	public void setGeo_ini(Integer geo_ini) {
		this.geo_ini = geo_ini;
	}
	public String getGeo_nom_ini() {
		return geo_nom_ini;
	}
	public void setGeo_nom_ini(String geo_nom_ini) {
		this.geo_nom_ini = geo_nom_ini;
	}
	public Integer getGeo_fin() {
		return geo_fin;
	}
	public void setGeo_fin(Integer geo_fin) {
		this.geo_fin = geo_fin;
	}
	public String getPatente() {
		return patente;
	}
	public void setPatente(String patente) {
		this.patente = patente;
	}
	public Long getVuelta() {
		return vuelta;
	}
	public void setVuelta(Long vuelta) {
		this.vuelta = vuelta;
	}
	public Long getSegundociclo() {
		return segundociclo;
	}
	public void setSegundociclo(Long segundociclo) {
		this.segundociclo = segundociclo;
	}
	public double getCarga() {
		return carga;
	}
	public void setCarga(double carga) {
		this.carga = carga;
	}
	public String getGeo_nom_fin() {
		return geo_nom_fin;
	}
	public void setGeo_nom_fin(String geo_nom_fin) {
		this.geo_nom_fin = geo_nom_fin;
	}
	

}
