package cl.samtech.sgomt.object;

public class PatentesActive {
	
	private String idPatente;
	private String patente;
	private String nInterno;
	
	
	public String getIdPatente() {
		return idPatente;
	}
	public void setIdPatente(String idPatente) {
		this.idPatente = idPatente;
	}
	public String getPatente() {
		return patente;
	}
	public void setPatente(String patente) {
		this.patente = patente;
	}
	public String getnInterno() {
		return nInterno;
	}
	public void setnInterno(String nInterno) {
		this.nInterno = nInterno;
	}

}
