package cl.samtech.sgomt.object;

public class MenuForm {
	
	String menId;
	String menNombre;
	String menLink;
	String modulo;
	String estado;
	String menupadre;
	String menus;
	
	public String getMenus() {
		return menus;
	}
	public void setMenus(String menus) {
		this.menus = menus;
	}
	public String getMenupadre() {
		return menupadre;
	}
	public void setMenupadre(String menupadre) {
		this.menupadre = menupadre;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getMenId() {
		return menId;
	}
	public void setMenId(String menId) {
		this.menId = menId;
	}
	public String getMenNombre() {
		return menNombre;
	}
	public void setMenNombre(String menNombre) {
		this.menNombre = menNombre;
	}
	public String getMenLink() {
		return menLink;
	}
	public void setMenLink(String menLink) {
		this.menLink = menLink;
	}
	public String getModulo() {
		return modulo;
	}
	public void setModulo(String modulo) {
		this.modulo = modulo;
	}
	
	

}
